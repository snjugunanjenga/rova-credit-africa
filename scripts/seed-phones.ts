async function seedPhones() {
  console.log(`Seeding ${allPhones.length} phones to Sanity (project: ${projectId}, dataset: ${dataset})...\n`)

  const transaction = client.transaction()

  for (const phone of allPhones) {
    const id = `phone-${slugify(phone.brand)}-${slugify(phone.asset_model)}`
    const slug = slugify(`${phone.brand}-${phone.name}`)

    const doc = {
      _id: id,
      _type: 'phone' as const,
      name: phone.name,
      slug: { _type: 'slug' as const, current: slug },
      brand: phone.brand,
      category: phone.category,
      price: phone.price,
      assetPrice: phone.asset_price,
      downPayment: phone.down_payment,
      assetModel: phone.asset_model,
      image: phone.image,
      alt: phone.alt,
      available: phone.available,
      rating: phone.rating,
      specs: phone.specs,
      badges: phone.badges,
      sortOrder: phone.sort_order,
    }

    transaction.createOrReplace(doc)
    console.log(`  ✓ ${phone.brand} - ${phone.name} (${id})`)
  }

  console.log('\nCommitting transaction...')
  const result = await transaction.commit()
  console.log(`\nDone! ${allPhones.length} phones seeded successfully.`)
  console.log(`Transaction ID: ${result.transactionId}`)

  const count = await client.fetch('count(*[_type == "phone"])')
  console.log(`\nVerification: ${count} phone documents in Sanity.`)
}

seedPhones().catch((err) => {
  console.error('Seed failed:', err)
  process.exit(1)
})
